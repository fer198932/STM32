// 脱机加工
void offlineWork(u32 num)
{
	offl_Data_Set(num);
	
	/* 加减速 */
	// 运动状态初始化
	AddSubSpeed_Init();
	
	// 步进电机同时开始运动
	StepMotor_Start();
	
	// 加减速定时器开启
	if(ENABLE != Offline_Work_Flag)
	{
		EN_ADDSUB_TIMER;
	}
}

